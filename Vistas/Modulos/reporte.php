<a name="SRathalos"></a>
<h1 class="titulo"> Set de Rathalos</h1>
</center>





<p class="contexto">
Este set esta hecho de partes del Rathalos, un enemigo formidable que a mas de uno tomo desprevenidos la primera
vez que lo enfrentaron, este set ofrece una gran resistencia a los ataques de fuego y una defensa superor al daño 
físico, ademas las armas de Rathalos poseen una afinidad positiva que ayuda a dar golpes críticos a los monstruos.


</p>

</div>



<center>
<a name="SRathian"></a>
<h1 class="titulo">Set de Rathian</h1>
</center>





<p class="contexto">
Este set hecho con partes de la Rathan no posee una defensa tan buena como la del Rathalos pero a cambio, este set 
hace que el cazador tenga una resistencia mayor al veveno por lo que le costara a los monstruos envenenarte a medio 
combate, además igual que el de Rathalos este set posee una buena resistenci al fuego, sus armas poseen la habilidad
estelar de la Rathian, envenenar, un buen set para los novatos en el juego.
 


</p>


<center>
<a name="SAnjanath"></a>
<h1 class="titulo">Set de Anjanath</h1>
</center>





<p class="contexto">
El set de Anjanath da a los cazadores uan formidable resistencia al fuego y a los tembores que ciertos modos pueden causar, es 
uno de los sets que por lo general los que inician una partida en  Monter Hunter World suelen hacerse enseguia por las altas defensas 
que otorga, sus armas poseen un ataque elemental de fuego mucho mayor que las de Rathalos pero poseen una afinidad 
negativa lo que hara casi imposible dar golpes críticos con ellas.

</p>

