<h1 class="titulo">Rathian</h1>
</center>





<p class="contexto">
La Rathian es el individuo femenino de la especie Rath, es la pareja del Rathalos y con quien tiene crias, se
destaca del Rathalos por su coloracion verde, su barbilla que termina en una púa y su cola llena de espinas
venenosas. Al contrario que el Rathalos esta prefier permanecer en tierra y mus pocas veces alza vuelo en un
combate, por lo general ronda la zonas circundantes a su nido, es mu protectora con sus huevos por lo que si
te atreves a tocar uno de ellos, ten por seguro que te perseguira hasta el fin del mundo con tal de recuperar
a sus preciadas crias. Solo la pueden enfrentar cazadores de Rango Alto.



</p>


