<a name="Anjanath"></a>
<h1 class="titulo">Anjanath</h1>
</center>





<p class="contexto">
Wyvern Brutal de patas traceras muy fuertes, tiene el aspecto de un tiranosaurio-rex, su coloracion es de un color rosado
y su lomo esta cubuerto de unas finas plumas negras que abarcan hasta su cola, es capaz de lanzar una llamarada por su boca
y posee un enorme hueso nasal que expone al entrar en estado de furia para intimidar, puede dar grandes saltos para acortar
distancias entre su objetivo y acestarle una mordida muy fuerte. Es recomedable ser un cazador de Rango Alto para cazar una 
de estas bestias pero un cazador de Rango Bajo puede contra el, aun que se recomienda ir con mucho cuiado.

</p>


